#' Function to get Air Quality e-Reporting (AQER) countries. 
#' 
#' @author Stuart K. Grange
#' 
#' @return Tibble. 
#' 
#' @examples 
#' 
#' # Get countries
#' data_countries <- aqer_countries()
#'
#' @export
aqer_countries <- function() {
  
  aqer_metadata() %>% 
    select(country = Countrycode) %>% 
    distinct(country) %>% 
    left_join(read_country_names(), by = "country")
  
}


